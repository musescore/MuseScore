//   fallbackFont
//---------------------------------------------------------

ScoreFont* ScoreFont::fallbackFont()
      {
      ScoreFont* f = &_builtinScoreFonts[FALLBACK_FONT];
      if (!f->face)
            f->load();
      return f;
      }

